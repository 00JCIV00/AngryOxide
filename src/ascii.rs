use rand::Rng;

pub fn get_art(text: &str) -> String {
    let ascii_art = get_random_art();
    let max_width = ascii_art.lines().map(|line| line.len()).max().unwrap_or(0);
    format!(
        "{}\n{:^width$} ",
        ascii_art,
        format!("========== {} ==========", text),
        width = max_width
    )
}

